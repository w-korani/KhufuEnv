bamSeqCigar(){
bam=$1
"$khufu_dir"/utilities/bamSeqCigar -bam $bam
}
###
bamSeqCigar2fasta(){
bam=$1
"$khufu_dir"/utilities/bamSeqCigar -bam $bam | sed '1d' | awk '{print ">"$1"_"$2"_"$3"_"$4"\n"$5}'
}
###
seq2cigar(){
seq=$1
cigar=$2
"$khufu_dir"/utilities/convert_cigar -seq $seq -cigar $cigar 
}
